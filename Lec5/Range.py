#=========================================================================#
# range(Start?, Stop, Step?)
for i in range(10): #range(stop)
    print(i)
print("")

for n in range(2,10): #range(start,stop)
    print(n)
print("")

for n in range(2,10,2): #range(start,stop,step)
    print(n)
print("")

for p in range(2,100,2):
    print(p)
